import 'package:flutter/material.dart';

// Function 'moreTextStyle' that returns a TextStyle object.
TextStyle moreTextStyle() {
  return const TextStyle(
    fontSize: 12, // Sets the font size to 12 pixels.
    fontFamily: 'Roboto', // Specifies the font family 'Roboto' for the text style.
    fontWeight: FontWeight.w400, // Sets the font weight to normal (400).
  );
}
